/*Squeeze theorem*/
#include <stdio.h>
#include <math.h>
double f(double x){
    return(x*x-2.0);
}
int main(void){
    int n=0;
    double a,b,c,d,fa,fb,fc,fd,er=1.0e-9;
    a=16.0;
    b=13.0;
    c=13.0;
    d=0.0;
    do {
        fa=f(a);
        fb=f(b);
        fc=f(c);
        d=(a*fc-c*fa)/(fc-fa);
        fd=f(d);
        if(fa*fd<0.0) {
            b=d;
        } else {
            a=d;
        }
        n++;
        printf("%2d : %.10lf(%.10lf)\n",n,d,fd);
        if(fabs(fd)<er) break;
    } while(fabs(a-b-c)>er);
}

